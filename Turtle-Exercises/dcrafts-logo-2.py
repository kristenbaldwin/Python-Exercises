def inverse():
    right(180)

def trapezoid(c):
    color(c)
    begin_fill()
    forward(100)
    left(120)
    forward(60)
    left(60)
    forward(50)
    left(60)
    forward(60)
    end_fill()

def pgram(c):
    color(c)
    begin_fill()
    forward(60)
    left(120)
    forward(60)
    left(60)
    forward(60)
    end_fill()

def small_eq_triangle(c):
    color(c)
    begin_fill()
    for i in range(3):
        forward(50)
        left(120)
    end_fill()

def big_eq_triangle(c):
    color(c)
    begin_fill()
    for i in range(3):
        forward(60)
        left(120)
    end_fill()


